<template>
  <v-container fluid>
    <BaseViewportHeader :selectable="false" />
    <BaseBreadcrumb>
      <template #extend>
        <v-flex class="kubegems__full-right">
          <v-btn class="primary--text" small text @click="resourceYaml">
            <v-icon left small> mdi-code-json </v-icon>
            YAML
          </v-btn>
          <v-menu v-if="m_permisson_resourceAllow()" left>
            <template #activator="{ on }">
              <v-btn icon>
                <v-icon color="primary" small v-on="on"> mdi-dots-vertical </v-icon>
              </v-btn>
            </template>
            <v-card>
              <v-card-text class="pa-2">
                <v-flex>
                  <v-btn color="primary" small text @click="updateCertificate"> {{ $root.$t('operate.edit') }} </v-btn>
                </v-flex>
                <v-flex>
                  <v-btn color="error" small text @click="removeCertificate"> {{ $root.$t('operate.delete') }} </v-btn>
                </v-flex>
              </v-card-text>
            </v-card>
          </v-menu>
        </v-flex>
      </template>
    </BaseBreadcrumb>
    <v-row class="mt-0">
      <v-col class="pt-0" cols="2">
        <BasicResourceInfo :item="certificate" />
      </v-col>
      <v-col class="pt-0" cols="10">
        <v-card flat>
          <v-card-text class="pa-0">
            <v-tabs v-model="tab" class="rounded-t pa-3" height="30">
              <v-tab v-for="item in tabItems" :key="item.value">
                {{ item.text }}
              </v-tab>
            </v-tabs>
          </v-card-text>
        </v-card>
        <component
          :is="tabItems[tab].value"
          :ref="tabItems[tab].value"
          class="mt-3"
          :item="certificate"
          :selector="selector"
        />
      </v-col>
    </v-row>

    <ResourceYaml ref="resourceYaml" :item="certificate" />
    <UpdateCertificate ref="updateCertificate" @refresh="certificateDetail" />
  </v-container>
</template>

<script>
  import { deleteCertificate, getCertificateDetail } from '@kubegems/api/direct';
  import BasicResourceInfo from '@kubegems/components/logicComponents/BasicResourceInfo';
  import EventList from '@kubegems/components/logicComponents/EventList';
  import Metadata from '@kubegems/components/logicComponents/Metadata';
  import ResourceYaml from '@kubegems/components/logicComponents/ResourceYaml';
  import BaseFilter from '@kubegems/mixins/base_filter';
  import BasePermission from '@kubegems/mixins/permission';
  import BaseResource from '@kubegems/mixins/resource';
  import { mapState } from 'vuex';

  import ResourceInfo from './components/ResourceInfo';
  import UpdateCertificate from './components/UpdateCertificate';

  export default {
    name: 'CertificateDetail',
    components: {
      BasicResourceInfo,
      EventList,
      Metadata,
      ResourceInfo,
      ResourceYaml,
      UpdateCertificate,
    },
    mixins: [BaseFilter, BasePermission, BaseResource],
    data() {
      return {
        certificate: null,
        tab: 0,
      };
    },
    computed: {
      ...mapState(['JWT', 'AdminViewport']),
      selector() {
        if (this.tabItems[this.tab].value === 'EventList') {
          return {
            topkind: 'Certificate',
            topname: this.certificate ? this.certificate.metadata.name : '',
          };
        }
        return {};
      },
      tabItems() {
        return [
          { text: this.$root.$t('tab.resource_info'), value: 'ResourceInfo' },
          { text: this.$root.$t('tab.metadata'), value: 'Metadata' },
          { text: this.$root.$t('tab.event'), value: 'EventList' },
        ];
      },
    },
    mounted() {
      this.$nextTick(() => {
        this.certificateDetail();
      });
    },
    methods: {
      async certificateDetail() {
        const data = await getCertificateDetail(this.ThisCluster, this.$route.query.namespace, this.$route.params.name);
        this.certificate = data;
      },
      resourceYaml() {
        this.$refs.resourceYaml.open();
      },
      updateCertificate() {
        this.$refs.updateCertificate.init(this.certificate);
        this.$refs.updateCertificate.open();
      },
      removeCertificate() {
        const item = this.certificate;
        this.$store.commit('SET_CONFIRM', {
          title: this.$root.$t('operate.delete_c', [this.$root.$t('resource.certificate')]),
          content: {
            text: `${this.$root.$t('operate.delete_c', [this.$root.$t('resource.certificate')])} ${item.metadata.name}`,
            type: 'delete',
            name: item.metadata.name,
          },
          param: { item },
          doFunc: async (param) => {
            await deleteCertificate(this.ThisCluster, this.$route.query.namespace, param.item.metadata.name);
            this.$router.push({
              name: 'certificate-list',
              params: this.$route.params,
            });
          },
        });
      },
    },
  };
</script>
